    private void tMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tMouseClicked
        // TODO add your handling code here:
        int rs = t.getSelectedRow();
        if(rs!=-1){
            txtid.setText(t.getValueAt(rs, 1).toString());
            txtname.setText(t.getValueAt(rs,2).toString());
            if(t.getValueAt(rs, 3).toString().equals("Male")){
                cboposition.setSelectedIndex(0);
            }
            
            else
                cboposition.setSelectedIndex(1);
            
            txtdob.setText(t.getValueAt(rs,4).toString());
            
            for(int i =0 ; i<cboposition.getItemCount(); i++){
                if(t.getValueAt(rs, 5).toString().equals(cboposition.getItemAt(i)))
                    cboposition.setSelectedIndex(i);
            }
            txtstart.setText(t.getValueAt(rs, 6).toString());
            txtsalary.setText(t.getValueAt(rs,7).toString());
        }
        
    }//GEN-LAST:event_tMouseClicked

