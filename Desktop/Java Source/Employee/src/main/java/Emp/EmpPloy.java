    private Employ AddEmpl(){
        String id = txtid.getText();
        String n = txtname.getText();
        String sex = cbosex.getSelectedItem().toString();
        String startwork = txtstart.getText();
        String pos = cboposition.getSelectedItem().toString();
        String dob = txtdob.getText();
        String sal = txtsalary.getText();
        if(id.length()==0)Msg("Input ID",txtid);
        else if(n.length()==0)Msg("Input Name ",txtname);
        else if(startwork.length()==0)Msg("Input Start work",txtstart);
        else if(dob.length()==0)Msg("Input Dob",txtdob);
        else if(sal.length()==0)Msg("Input Salary",txtsalary);
        else{
            try{
                double salary = Double.parseDouble(sal);
                return new Employ(id,n,sex,dob,pos,startwork,salary);
                
            }catch(Exception exp){
                txtsalary.selectAll();
                Msg("Input only Number",txtsalary);
                
                return null;
            }
        }// end else
        return null;
    }
