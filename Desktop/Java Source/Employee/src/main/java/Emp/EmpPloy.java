    private double getNumber(JTextField t){
        try{
            double n = Double.parseDouble(t.getText());
            return n;
        }catch(NumberFormatException exp){
            Msg("Input Number only",t);
            return 0;
        }
    }
   //---- Show data in Table
    private void ShowData(DBMS d,JTable ta){
        int rc = ta.getRowCount();
        for(int i = rc-1 ; i>=0 ; i--)
            model.removeRow(i);
        //ArrayList<Employ> ls = d.getAllEmploy();
        for(int i =0 ; i<d.getSize(); i++){
            Employ e = d.getEmployee(i);
            NumberFormat nf = NumberFormat.getCurrencyInstance(Locale.US);
            //-- add to model
            model.addRow(new Object[]{i+1,e.getID(),e.getName(),e.getSex(),
            e.getDob(),e.getStart(),e.getPosition(),nf.format(e.getSalary())});
        }
        t.setModel(model);
    }
    //--------
       private void Show(ArrayList<Employ> d,JTable ta){
        int rc = ta.getRowCount();
        for(int i = rc-1 ; i>=0 ; i--)
            model.removeRow(i);
        //ArrayList<Employ> ls = d.getAllEmploy();
        for(int i =0 ; i<d.size(); i++){
            Employ e = d.get(i);
            NumberFormat nf = NumberFormat.getCurrencyInstance(Locale.US);
            //-- add to model
            model.addRow(new Object[]{i+1,e.getID(),e.getName(),e.getSex(),
            e.getDob(),e.getStart(),e.getPosition(),nf.format(e.getSalary())});
        }
        t.setModel(model);
    } 
      
